package CreationShip.demo.models;

public class LongJSON {

   private Long id;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "LongJSON{" +
                "id=" + id +
                '}';
    }
}
